#!/usr/bin/env bash
# =============================================================================
#  ZSH Setup — Oh My Zsh + Starship + best plugins
# =============================================================================
set -euo pipefail

# ── Oh My Zsh ─────────────────────────────────────────────────────────────────
if [[ ! -d "$HOME/.oh-my-zsh" ]]; then
  echo "  ➜  Installing Oh My Zsh..."
  RUNZSH=no CHSH=no sh -c "$(curl -fsSL https://raw.githubusercontent.com/ohmyzsh/ohmyzsh/master/tools/install.sh)"
else
  echo "  ✔  Oh My Zsh already installed"
fi

ZSH_CUSTOM="${ZSH_CUSTOM:-$HOME/.oh-my-zsh/custom}"

# ── Plugins ───────────────────────────────────────────────────────────────────
install_omz_plugin() {
  local name="$1" repo="$2"
  if [[ ! -d "$ZSH_CUSTOM/plugins/$name" ]]; then
    echo "  ➜  Installing plugin: $name"
    git clone --depth=1 "$repo" "$ZSH_CUSTOM/plugins/$name"
  else
    echo "  ✔  Plugin already installed: $name"
  fi
}

install_omz_plugin "zsh-autosuggestions"    "https://github.com/zsh-users/zsh-autosuggestions"
install_omz_plugin "zsh-syntax-highlighting" "https://github.com/zsh-users/zsh-syntax-highlighting"
install_omz_plugin "zsh-completions"         "https://github.com/zsh-users/zsh-completions"
install_omz_plugin "zsh-history-substring-search" "https://github.com/zsh-users/zsh-history-substring-search"

# ── Starship Prompt ───────────────────────────────────────────────────────────
if ! command -v starship &>/dev/null; then
  echo "  ➜  Installing Starship prompt..."
  curl -sS https://starship.rs/install.sh | sh -s -- --yes
fi

# ── Starship Config ───────────────────────────────────────────────────────────
mkdir -p "$HOME/.config"
cat > "$HOME/.config/starship.toml" << 'STARSHIP'
"$schema" = 'https://starship.rs/config-schema.json'

format = """
[░▒▓](#a3aed2)\
[  ](bg:#a3aed2 fg:#090c0c)\
[](bg:#769ff0 fg:#a3aed2)\
$directory\
[](fg:#769ff0 bg:#394260)\
$git_branch\
$git_status\
[](fg:#394260 bg:#212736)\
$nodejs\
$python\
$java\
$kotlin\
$aws\
[](fg:#212736 bg:#1d2230)\
$time\
[ ](bg:#1d2230)\
\n$character"""

[directory]
style = "fg:#e3e5e5 bg:#769ff0"
format = "[ $path ]($style)"
truncation_length = 3
truncation_symbol = "…/"

[git_branch]
symbol = ""
style = "bg:#394260"
format = '[[ $symbol $branch ](fg:#769ff0 bg:#394260)]($style)'

[git_status]
style = "bg:#394260"
format = '[[($all_status$ahead_behind )](fg:#769ff0 bg:#394260)]($style)'

[nodejs]
symbol = ""
style = "bg:#212736"
format = '[[ $symbol ($version) ](fg:#769ff0 bg:#212736)]($style)'

[python]
symbol = ""
style = "bg:#212736"
format = '[[ $symbol ($version) ](fg:#769ff0 bg:#212736)]($style)'

[java]
symbol = ""
style = "bg:#212736"
format = '[[ $symbol ($version) ](fg:#769ff0 bg:#212736)]($style)'

[kotlin]
symbol = ""
style = "bg:#212736"
format = '[[ $symbol ($version) ](fg:#769ff0 bg:#212736)]($style)'

[aws]
symbol = "☁️"
style = "bg:#212736"
format = '[[ $symbol ($profile)(\($region\)) ](fg:#769ff0 bg:#212736)]($style)'

[time]
disabled = false
time_format = "%R"
style = "bg:#1d2230"
format = '[[  $time ](fg:#a0a9cb bg:#1d2230)]($style)'
STARSHIP

echo "  ✔  Starship config written"

# ── Nerd Font ─────────────────────────────────────────────────────────────────
echo "  ➜  Installing JetBrains Mono Nerd Font..."
FONT_DIR="$HOME/.local/share/fonts"
mkdir -p "$FONT_DIR"
if [[ ! -f "$FONT_DIR/JetBrainsMonoNerdFont-Regular.ttf" ]]; then
  FONT_URL="https://github.com/ryanoasis/nerd-fonts/releases/latest/download/JetBrainsMono.tar.xz"
  curl -sL "$FONT_URL" -o /tmp/JetBrainsMono.tar.xz
  tar -xf /tmp/JetBrainsMono.tar.xz -C "$FONT_DIR"
  rm -f /tmp/JetBrainsMono.tar.xz
  fc-cache -f "$FONT_DIR"
  echo "  ✔  JetBrains Mono Nerd Font installed"
else
  echo "  ✔  JetBrains Mono Nerd Font already installed"
fi

# ── .zshrc ────────────────────────────────────────────────────────────────────
echo "  ➜  Writing ~/.zshrc..."

cat > "$HOME/.zshrc" << 'ZSHRC'
# =============================================================================
#  ~/.zshrc — Generated by ubuntu-setup
# =============================================================================

# ── Oh My Zsh ─────────────────────────────────────────────────────────────────
export ZSH="$HOME/.oh-my-zsh"
ZSH_THEME=""   # Using Starship instead

plugins=(
  git
  gh
  docker
  docker-compose
  aws
  python
  pip
  pyenv
  node
  npm
  gradle
  mvn
  vscode
  ubuntu
  zsh-autosuggestions
  zsh-syntax-highlighting
  zsh-completions
  zsh-history-substring-search
  fzf
  sudo          # press ESC twice to add sudo to last command
  copypath      # copy current path to clipboard
  web-search    # 'google something' from terminal
  z             # jump to frecent dirs
)

source "$ZSH/oh-my-zsh.sh"

# ── Starship Prompt ───────────────────────────────────────────────────────────
eval "$(starship init zsh)"

# ── PATH ──────────────────────────────────────────────────────────────────────
export PATH="$HOME/.local/bin:$HOME/bin:/usr/local/bin:$PATH"

# ── Java / Kotlin ─────────────────────────────────────────────────────────────
export JAVA_HOME="/usr/lib/jvm/java-21-openjdk-$(dpkg --print-architecture 2>/dev/null || echo amd64)"
export PATH="$JAVA_HOME/bin:$PATH"

# ── SDKMAN (Gradle, Kotlin) ──────────────────────────────────────────────────
[[ -s "$HOME/.sdkman/bin/sdkman-init.sh" ]] && source "$HOME/.sdkman/bin/sdkman-init.sh"

# ── pyenv ─────────────────────────────────────────────────────────────────────
export PYENV_ROOT="$HOME/.pyenv"
export PATH="$PYENV_ROOT/bin:$PATH"
eval "$(pyenv init -)"

# ── nvm ───────────────────────────────────────────────────────────────────────
export NVM_DIR="$HOME/.nvm"
[ -s "$NVM_DIR/nvm.sh" ] && source "$NVM_DIR/nvm.sh"
[ -s "$NVM_DIR/bash_completion" ] && source "$NVM_DIR/bash_completion"

# ── zoxide (smarter cd) ───────────────────────────────────────────────────────
eval "$(zoxide init zsh)"

# ── fzf ───────────────────────────────────────────────────────────────────────
export FZF_DEFAULT_COMMAND='fd --type f --hidden --follow --exclude .git'
export FZF_DEFAULT_OPTS='--height 40% --layout=reverse --border'

# ── History ───────────────────────────────────────────────────────────────────
HISTSIZE=50000
SAVEHIST=50000
HISTFILE=~/.zsh_history
setopt HIST_IGNORE_DUPS
setopt HIST_IGNORE_SPACE
setopt SHARE_HISTORY
setopt EXTENDED_HISTORY

# ── Aliases — Navigation ──────────────────────────────────────────────────────
alias ..='cd ..'
