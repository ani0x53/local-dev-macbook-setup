# ── Functions ─────────────────────────────────────────────────────────────────

# mkcd: make directory and cd into it
mkcd() { mkdir -p "$@" && cd "$_"; }

# extract: universal archive extractor
extract() {
  if [ -f "$1" ]; then
    case "$1" in
      *.tar.bz2)  tar xjf "$1"  ;;
      *.tar.gz)   tar xzf "$1"  ;;
      *.tar.xz)   tar xJf "$1"  ;;
      *.bz2)      bunzip2 "$1"  ;;
      *.gz)       gunzip "$1"   ;;
      *.tar)      tar xf "$1"   ;;
      *.tbz2)     tar xjf "$1"  ;;
      *.tgz)      tar xzf "$1"  ;;
      *.zip)      unzip "$1"    ;;
      *.Z)        uncompress "$1" ;;
      *.7z)       7z x "$1"    ;;
      *.deb)      sudo dpkg -i "$1" ;;
      *)          echo "'$1' cannot be extracted" ;;
    esac
  else
    echo "'$1' is not a valid file"
  fi
}

# port: show what's on a port
port() { ss -tlnp | grep ":${1}"; }

# killport: kill process on a port
killport() { fuser -k "${1}/tcp" 2>/dev/null || echo "Nothing on port $1"; }

# weather: show weather
weather() { curl -s "wttr.in/${1:-}?format=3"; }

# newpr: open new GitHub PR in browser
newpr() { gh pr create --web; }

# jdk: switch Java version
jdk() {
  local version=$1
  export JAVA_HOME="/usr/lib/jvm/java-${version}-openjdk-$(dpkg --print-architecture)"
  export PATH="$JAVA_HOME/bin:$PATH"
  java -version
}

# ── Editor ────────────────────────────────────────────────────────────────────
export EDITOR='code --wait'
export VISUAL="$EDITOR"

# ── Auto-completions ──────────────────────────────────────────────────────────
autoload -Uz compinit && compinit
zstyle ':completion:*' menu select
zstyle ':completion:*' matcher-list 'm:{a-z}={A-Z}'

# ── Key Bindings ──────────────────────────────────────────────────────────────
bindkey '^[[A' history-substring-search-up
bindkey '^[[B' history-substring-search-down
bindkey "^[[1;5C" forward-word   # Ctrl+Right
bindkey "^[[1;5D" backward-word  # Ctrl+Left

# ── Local overrides ───────────────────────────────────────────────────────────
[ -f "$HOME/.zshrc.local" ] && source "$HOME/.zshrc.local"
ZSHRC

echo "  ✔  ~/.zshrc written"

# ── .zprofile ─────────────────────────────────────────────────────────────────
echo "  ➜  Writing ~/.zprofile..."
cat > "$HOME/.zprofile" << 'ZPROFILE'
# pyenv — must be in .zprofile so shims are available in non-interactive shells
export PYENV_ROOT="$HOME/.pyenv"
export PATH="$PYENV_ROOT/bin:$PATH"
eval "$(pyenv init --path)"
ZPROFILE
echo "  ✔  ~/.zprofile written"

# Set zsh as default shell
if [[ "$SHELL" != "$(which zsh)" ]]; then
  echo "  ➜  Setting ZSH as default shell..."
  chsh -s "$(which zsh)" || true
fi

echo "  ✔  ZSH setup complete"
